function buildKaedeRgba(): Uint8ClampedArray {
  const buf = new Uint8ClampedArray(KAEDE_WIDTH * KAEDE_HEIGHT * 4);
  for (let row = 0; row < KAEDE_HEIGHT; row++) {
    const rp = KAEDE_PIXELS[row];
    if (!rp) continue;
    for (let col = 0; col < KAEDE_WIDTH; col++) {
      const idx = rp[col];
      if (idx === undefined || idx < 0) continue;
      const c = KAEDE_PALETTE[idx];
      if (!c) continue;
      writeRgba(buf, KAEDE_WIDTH, col, row, c);
    }
  }
  return buf;
}

export const KAEDE_RGBA = buildKaedeRgba();
