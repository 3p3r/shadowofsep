function buildHayateRgba(): Uint8ClampedArray {
  const buf = new Uint8ClampedArray(HAYATE_WIDTH * HAYATE_HEIGHT * 4);
  for (let row = 0; row < HAYATE_HEIGHT; row++) {
    const rp = HAYATE_PIXELS[row];
    if (!rp) continue;
    for (let col = 0; col < HAYATE_WIDTH; col++) {
      const idx = rp[col];
      if (idx === undefined || idx < 0) continue;
      const c = HAYATE_PALETTE[idx];
      if (!c) continue;
      writeRgba(buf, HAYATE_WIDTH, col, row, c);
    }
  }
  return buf;
}

export const HAYATE_RGBA = buildHayateRgba();
